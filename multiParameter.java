public class multiParameter {
    static void myMethod(java.lang.String string, int age) {
        System.out.println(string + " is " + age);
    }

    public static void main(String[] args) {
        myMethod("Shourav", 20);
        myMethod("Shahed", 19);
        myMethod("Salman", 21);
        myMethod("Sabbir", 22);
    }
}
